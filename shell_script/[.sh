[

 
 
[ var1 -eq var2 ]
-true if var1 = var2
 
[ var1 -ne var2 ]
-true if var1 != var2
 
[ var1 -le var2 ]
-true if var1 <= var2
 
[ var1 -lt var2 ]
-true if var1 < var2
 
[ var1 -ge var2 ]
-true if var1 >= var2
 
[ var1 -gt var2 ]
-true if var1 > var2
 
[ -e file_name ]
-true if file_name exists

[ -e "file" ]
-return true if "file" exists
-false otherwise

[ "$s1" = "$s2" ]
-return true if s1 = s2

[ "$s1" != "$s2" ]
-return true if s1 != s2
