g++

g++ [file]
-compile [file] with g++ compiler

g++ -c

g++ [file] -o [name]
-compile [file] with g++ and name the executable program [name]

g++ [file] -std=c++14
-compile [file] with g++ and use c++14 as the standard

g++ -Wall [file]
-compile [file] with g++ and enable all warnings

g++ -w [file]
-compile [file] with no warning info

g++ -v
-check the current version of "g++"

g++ -c [file]
-compile [file] to a .o file

g++ -pedantic [file]
-compile [file] and show all the warnings according to the ISO standard
