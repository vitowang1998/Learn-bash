ls



ls
-list all (non-hidden) files in current folder

ls -a
-a stands for "all"
-list all files (including hidden files)
-hidden files' name starts with a dot(.)

ls -l
-list all files along with permission information
-l stands for "long"

ls *.txt
-wildcard matching
-lists all files of the form [whatever].txt

ls -1
-list all files in current folder.
-every filename is in a single line

ls -lh (or ls -l -h)
-list all files along with permission information
-change the size of file to human-readable words

ls -alh (or ls -a -l -h)
-list all files (including hidden files)
-list all files (including hidden files)
-change the size of file to human-readable words

ls [GLOBBING PATTERN]
-list all files that conform to [GLOBBING PATTERN]
